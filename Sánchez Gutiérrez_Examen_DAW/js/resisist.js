function resistencia1() {
    res1++;
    result = document.getElementById("resistencia1");
    switch (res1) {
        case 1:
          
           result.style.background = "brown";
            break;

            case 2:
            result.style.background = "red";
            break;

            case 3:
                result.style.background = "orange";
            break;

            case 4:
                result.style.background = "yellow";
            break;

            case 5:
                result.style.background = "green";
            break;
            case 6:
                result.style.background = "blue";
            break;
            case 7:
                result.style.background = "violet";
            break;
            case 8:
                result.style.background = "gray";
            break;
            case 9:
                result.style.background = "white";
                res1 = 0;
            break;
        default:
            result.style.background = "black";
            break;
    }
}

